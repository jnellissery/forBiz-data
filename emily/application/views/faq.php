                <p><strong>8. How often should I see my doctor once Emily is placed?</strong></p>
                <div class="margleft">
                    <p>You should have the system checked by your doctor approximately 4 to 12 weeks after it is fitted, again at 12 months and then once a year until it is removed. EMILY can stay in place for 3 years.</p>
				</div>

                <p><strong>9. What should I do, if I wish to become pregnant?</strong></p>
                <div class="margleft">
                    <p>If you wish to become pregnant, ask your doctor to remove EMILY. Your usual level of fertility should return soon after the system is removed.</p>
				</div>

                <p><strong>10. Will my partner be able to feel Emily during intercourse?</strong></p>
                <div class="margleft">
                    <p>During sexual intercourse, you or your partner should not be able to feel EMILY. If you can feel EMILY, or any pain or discomfort that you suspect may be caused by it, then you should not have sexual intercourse until you consult your doctor to verify it is still in the correct position.</p>
				</div>


                <p><strong>11.	Will Emily protect from HIV or STDs (sexually transmitted diseases)?</strong></p>
                <div class="margleft">
                    <p>No. Emily (Levonorgestrel-releasing Intrauterine System) does not protect against HIV or STDs. So, if while using Emily you should think you or your partner is at a risk of getting an STD, use a condom and call your healthcare provider.</p>
