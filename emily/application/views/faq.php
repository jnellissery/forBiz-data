        		<h1>FAQ's</h1>
	  			
                <p><strong>1. What is Emily (Levonorgestrel-releasing Intrauterine System)? How does Emily prevent pregnancy?</strong></p>
                <div class="margleft">
                    <p>EMILY is an intrauterine delivery system (IUS) consisting of a small white M-shaped frame made from soft, flexible plastic. The vertical arm is surrounded by a narrow cylindrical shaped reservoir that contains Levonorgestrel which slowly releases into your uterus. It prevents pregnancy and decrease the abnormally heavy menstrual blood loss.<br>EMILY works by slowly releasing Levonorgestrel into the uterus at a rate of approximately 20 micrograms per day. </p>
                    <p><span>This amount of Levonorgestrel:</span></p>
                    <ul>
                        <li>Reduces the normal monthly thickening of the lining of the uterus.</li>
                        <li>Thickens the cervical mucus which prevents passage of sperm through the cervical canal (opening to the uterus).</li>
                    </ul>
				</div>

                <p><strong>2. Is Emily effective in treating Heavy Menstrual bleeding?</strong></p>
                <div class="margleft">
                    <p>Yes, Emily is effective in treating heavy menstrual bleeding (DUB) in globally.</p>
				</div>
	
                <p><strong>3. Why should I prefer Emily?</strong></p>
                <div class="margleft">
                    <ul>
                        <li>Long term Contraception and effective treatment for DUB.</li>
                        <li>Less systemic side effects like oral contraceptives.</li>
                        <li>Easy insertion with less pain.</li>
                        <li>Available at affordable cost.</li>
                    </ul>
				</div>
           
                <p><strong>4. Is it a difficult process to insert Emily?</strong></p>
                <div class="margleft">
                    <p>No, The insertion procedure usually takes a few minutes after your doctor has completed the pelvic examination. You will be protected from pregnancy as soon as insertion of the system is complete; however, it is best to wait 24 to 48 hours before having sexual intercourse. A reduction in menstrual blood loss should be apparent from the first menstrual cycle.</p>
				</div>
            
                <p><strong>5. Will my periods change with Emily?</strong></p>
                <div class="margleft">
                    <p>EMILY will affect your menstrual cycle. You might experience frequent spotting (a small amount of blood loss) or light bleeding in addition to your periods for the first 3 to 6 months. In some cases, you may have heavy or prolonged bleeding during this time. </p>
                    <p>Overall, gradually, your menstrual period may disappear when using EMILY. Due to the hormone’s effect on the lining of the uterus it’s become thicken. Therefore there is little or no bleeding, as happens during a usual menstrual period. But, when the system is removed, periods should return to normal. Menstruation disappearance does not necessarily mean you have reached menopause or are pregnant. If, however, you are having regular menstrual periods and then do not have one for 6 weeks or longer, it is possible that you may be pregnant. You should consult with your doctor. 

</p>
				</div>


                <p><strong>6. Which are the conditions were Emily should not be used?</strong></p>
                <div class="margleft">
                    <p>EMILY is not suitable for every woman. Your doctor can advise you if you have any conditions that would pose a risk to you. You should not use EMILY if you:

 </p>
                    <ul>
                        <li>have any allergies to the hormone levonorgestrel, or to any of the other ingredients of EMILY, </li>
                        <li>are pregnant, or if you suspect that you may be pregnant,</li>
                        <li>Currently have pelvic inflammatory disease (PID) or have a recurrent PID  </li>
                        <li>had an infection of the uterus (womb)/cervix after delivering a baby or having abortion during the past 3 months</li>
                        <li>have a condition of the uterus that distorts the uterine cavity, such as large fibroids </li>
                        <li>have cell abnormalities in the cervix (your doctor can tell you if you have this)</li>
                        <li>have a known or suspected progestogen-dependent tumour, including breast cancer</li>
                        <li>have liver disease or liver tumor</li>
                        <li>have bacterial endocarditis (an infection of the heart valves or lining of the heart)</li>
                        <li>have immunodeficiency (a doctor will have told you if you have this)</li>
                        <li>have cancer affecting the blood, or if you have leukemia</li>
                        <li>have or have had trophoblastic disease (a doctor will have told you if you have this)</li>
                        <li>have cancer of the uterus or the cervix (uterine or cervical malignancy. </li>
                    </ul>
				</div>

                <p><strong>7. Are there any potential serious complications and any side effects with Emily?</strong></p>
                <div class="margleft">
                    <p>Pelvic inflammatory disease (PID). Some IUD users get a serious pelvic infection called pelvic inflammatory disease. PID is usually sexually transmitted. You have a higher chance of getting PID if you or your partner has sex with other partners. PID can cause serious problems such as infertility, ectopic pregnancy or pelvic pain that does not go away. PID is usually treated with antibiotics. More serious cases of PID may require surgery. A hysterectomy (removal of the uterus) is sometimes needed. In rare cases, infections that start as PID can even cause death.</p>
                    <p>Tell your healthcare provider right away if you have any of these signs of PID: long-lasting or heavy bleeding, unusual vaginal discharge, low abdominal (stomach area) pain, painful sex, chills, or fever.</p>
                    <p><i><u>Embedment</u></i>&nbsp;: Emily may become attached to the uterine wall. This is called embedment. If embedment happens, Emily may no longer prevent pregnancy and you may need surgery to have it removed.</p>
                    <p><i><u>Perforation</u></i>&nbsp;: Emily may go through the uterus. This is called perforation. If your uterus is perforated, Emily may no longer prevent pregnancy. It may move outside the uterus and can cause internal Scarring, infection, or damage to other organs, and you may need surgery to have Emily removed.</p>
